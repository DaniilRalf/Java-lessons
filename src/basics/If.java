package basics;

public class If {
    static public void main(String [] args){

        int value = 10;
        if (value == 10){
            System.out.println("Value is true");
        } else if (value == 5) {
            System.out.println("Value is 5");
        } else {
            System.out.println("Value is not true");
        }
    }
}
